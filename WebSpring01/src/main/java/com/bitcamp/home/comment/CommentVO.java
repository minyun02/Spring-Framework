package com.bitcamp.home.comment;

public class CommentVO {
	private int no;
	private String useridC;
	private String cntentC;
	private String writedateC;
	
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getUseridC() {
		return useridC;
	}
	public void setUseridC(String useridC) {
		this.useridC = useridC;
	}
	public String getCntentC() {
		return cntentC;
	}
	public void setCntentC(String cntentC) {
		this.cntentC = cntentC;
	}
	public String getWritedateC() {
		return writedateC;
	}
	public void setWritedateC(String writedateC) {
		this.writedateC = writedateC;
	}
}
